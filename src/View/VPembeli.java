    void isitabel(){
        Object data[][] = new Object[record.size()][3];
        int x = 0;
        for(MPembeli pembeli : record){
            data[x][0] = pembeli.getPembeliID();
            data[x][1] = pembeli.getPembeliNama();
            data[x][2] = pembeli.getNomorHP();
            x++;
        }
        String judul[] = {"ID_Pembeli", "Nama_Pembeli", "Nomor_HP"};
        jTable1.setModel(new DefaultTableModel (data, judul));
    }
    
    void refreshdata(){
        panggildata();
        isitabel();
    }
    
    void bersih(){
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField1.setEnabled(true);
        jTextField1.requestFocus();
    }


