    void isitabel(){
        Object data[][] = new Object[record.size()][7];
        int x = 0;
        for(MTransaksi transaksi : record){
            data[x][0] = transaksi.getTransaksiKode();
            data[x][1] = transaksi.getPenjualID();
            data[x][2] = transaksi.getPembeliID();
            data[x][3] = transaksi.getLaptopKode();
            data[x][4] = transaksi.getHarga();
            data[x][5] = transaksi.getTransaksiTgl();
            data[x][6] = transaksi.getStok();
            x++;
        }
        String judul[] = {"Kode_Transaksi", "ID_Penjual", "ID_Pembeli", "Kode_Laptop", "Harga", "Tanggal_Transaksi", "Stok"};
        jTable1.setModel(new DefaultTableModel (data, judul));
    }
    
    void refreshdata(){
        panggildata();
        isitabel();
    }
    
