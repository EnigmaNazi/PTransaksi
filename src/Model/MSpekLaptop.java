public class MSpekLaptop {
    private String LaptopKode, Merek, RAM, Hardisk, Processor, OS, GPU;

    public String getLaptopKode() {
        return LaptopKode;
    }

    public void setLaptopKode(String LaptopKode) {
        this.LaptopKode = LaptopKode;
    }

    public String getMerek() {
        return Merek;
    }

    public void setMerek(String Merek) {
        this.Merek = Merek;
    }

    public String getRAM() {
        return RAM;
    }

    public void setRAM(String RAM) {
        this.RAM = RAM;
    }

    public String getHardisk() {
        return Hardisk;
    }

    public void setHardisk(String Hardisk) {
        this.Hardisk = Hardisk;
    }

    public String getProcessor() {
        return Processor;
    }

    public void setProcessor(String Processor) {
        this.Processor = Processor;
    }

    public String getOS() {
        return OS;
    }

    public void setOS(String OS) {
        this.OS = OS;
    }

    public String getGPU() {
        return GPU;
    }

    public void setGPU(String GPU) {
        this.GPU = GPU;
    }
    
}
